function parseProducts(): Product[] {
  const lines = menuText.split('\n').filter(line => line.trim() !== '');
  const products: Product[] = [];
  let currentCategory = '';
  let id = 1;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();

    // Check if it's a category header
    if (line.toUpperCase() === line && !line.includes('PEÇAS') && !line.includes('UNI') && !line.includes('G') && line.length > 3) {
      currentCategory = line;
      continue;
    }

    // Check if it's a product line (contains description and might have price)
    if (line.includes('-') && (line.includes('PEÇAS') || line.includes('UNI') || line.includes('G'))) {
      const parts = line.split(' - ');
      if (parts.length >= 2) {
        const name = parts[0].trim();
        const description = parts.slice(1).join(' - ').trim();

        // Extract price if present (look for € or numbers at the end)
        let price = '';
        const priceMatch = description.match(/(\d+(?:\.\d+)?)\s*€?$/);
        if (priceMatch) {
          price = priceMatch[1] + '€';
        }

        products.push({
          id,
          name,
          description: price ? description.replace(/\s*\d+(?:\.\d+)?\s*€?$/, '').trim() : description,
          price: price || 'Preço sob consulta',
          category: currentCategory,
          image: `/${id}.webp`
        });

        id++;
      }
    }
  }

  return products;
}

